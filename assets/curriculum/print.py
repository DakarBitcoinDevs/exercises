# print your name
print('justin')

# print your first and last name on the same line
print('justin moon')

# print your first and last name on separate lines
print('justin')
print('moon')

# print you last name first
print('moon,')
print('justin')

# there are special characters
print('justin\nmoon')

# how to use a single-quote inside a string?
# magic backslash
print('justin\'s bitcoins')

# or use double-quotes
print("justin's moon")
